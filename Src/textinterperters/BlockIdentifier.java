package textinterperters;

/**
 * The enum Block identifier.
 */
public enum BlockIdentifier {
    /**
     * Symbol block identifier.
     */
    SYMBOL,
    /**
     * Height block identifier.
     */
    HEIGHT,
    /**
     * Width block identifier.
     */
    WIDTH,
    /**
     * Hit points block identifier.
     */
    HIT_POINTS,
    /**
     * Color block identifier.
     */
    COLOR,
    /**
     * Stroke block identifier.
     */
    STROKE,
    /**
     * Image block identifier.
     */
    IMAGE
}
